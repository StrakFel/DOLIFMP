while True:
    try:
        n = int(input("Введіть кількість кіл (від 1 до 10^8): "))
        if 1 <= n <= 10 ** 8:
            res = n * n - n + 2
            print(f"Максимальна кількість частин площини: {res}")
            break
        else:
            print("Введено некоректне значення, спробуйте ще раз")
    except ValueError:
        print("Помилка - потрібно ввести лише ціле число")