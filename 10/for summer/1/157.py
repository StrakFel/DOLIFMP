while True:
    try:
        n = int(input("Введіть кількість кліток у зоопарку (від 2 до 100): "))
        if 2 <= n <= 100:
            res = (n - 1) * (n - 2)
            print(f"Кількість способів: {res}")
            break
        else:
            print("Введено некоректне значення, спробуйте ще раз")
    except ValueError:
        print("Помилка - потрібно ввести лише цілі числа")